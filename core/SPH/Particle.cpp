#include <Particle.h>
/****************************************************************************/
/****************************************************************************/
Particle::Particle()
{
    pos = vel = forces = Vector3f(0,0,0);
	mass = 0;
	color = Vector3f(0,0,1);
}
/****************************************************************************/
Particle::Particle(Vector3f pos, Vector3f vel, float mass)
{
	this->pos = pos;
	this->vel = vel;
	this->mass = mass;
    this->forces = Vector3f(0,0,0);
	this->color = Vector3f(0,0,1);
}
/****************************************************************************/
Particle::Particle(const Particle &P)
{
	pos = P.pos;
	vel = P.vel;
	forces =  P.forces;
	mass = P.mass;
	color = P.color;
}
/****************************************************************************/
Particle::~Particle()
{
}
/****************************************************************************/
/****************************************************************************/
Vector3f Particle::getPos()
{
	return pos;
}
/****************************************************************************/
Vector3f Particle::getVel()
{
	return vel;
}
/****************************************************************************/
Vector3f Particle::getForces()
{
	return forces;
}
/****************************************************************************/
Vector3f Particle::getColor()
{
	return color;
}
/****************************************************************************/
float Particle::getMass()
{
	return mass;
}
/****************************************************************************/
/****************************************************************************/
void Particle::setPos(Vector3f pos)
{
	this->pos = pos;
}
/****************************************************************************/
void Particle::setVel(Vector3f vel)
{
	this->vel = vel;
}
/****************************************************************************/
void Particle::setForces(Vector3f forces)
{
	this->forces = forces;
}
/****************************************************************************/
void Particle::setColor(Vector3f color)
{
	this->color = color;
}
/****************************************************************************/
void Particle::setMass(float mass)
{
	this->mass = mass;
}
/****************************************************************************/
void Particle::addForce(Vector3f force)
{
	this->forces += force;
}
/****************************************************************************/
/****************************************************************************/