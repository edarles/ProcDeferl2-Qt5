#include <Particle.h>
/****************************************************************************/
/****************************************************************************/
Particle::Particle()
{
}
/****************************************************************************/
Particle::Particle(Particle &P)
{
	pos = P.pos;
	vel = P.vel;
	forces =  P.forces;
	mass = P.mass;
	radius = P.radius;
	rho = P.rho;
	p = P.p;
	vois.clear();
	for(int i=0;i<P.vois.size();i++)
		vois.push_back(P.vois[i]);
}
/****************************************************************************/
Particle::~Particle()
{
	vois.clear();
	vois.shrink_to_fit();
	vois.~vector();
}
